/*
 * fluxvideotex.c — GStreamer BaseTransform element: fluxvideotex
 *
 * FLUX Protocol Spec v0.6.3 §16
 *   Resolves video_texture_bindings and flux:// image URIs, composites
 *   multi-channel bindings, uploads GPU textures (OpenGL / Vulkan).
 *
 * poc003 single-channel path:
 *   sink: video/x-raw, format=RGBA  (from videotestsrc via videoconvert)
 *   src : video/x-raw, format=RGBA  (rendered cube, to osxvideosink)
 *
 * Each incoming frame is uploaded as a Filament Texture onto all 6 faces of
 * a unit cube.  The cube rotates slowly on three axes (configurable periods)
 * to demonstrate the live-texture compositing core of fluxvideotex.
 */

